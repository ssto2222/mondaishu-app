# --- 2. データベース管理 (テキストファイル) ---
DB_FILE = "rouki_wrong_list.txt"

def load_wrong_ids():
    if os.path.exists(DB_FILE):
        with open(DB_FILE, "r") as f:
            return set(line.strip() for line in f)
    return set()

def save_wrong_id(qid):
    wrong_ids = load_wrong_ids()
    wrong_ids.add(str(qid))
    with open(DB_FILE, "w") as f:
        for i in wrong_ids:
            f.write(f"{i}\n")

def remove_wrong_id(qid):
    wrong_ids = load_wrong_ids()
    if str(qid) in wrong_ids:
        wrong_ids.remove(str(qid))
        with open(DB_FILE, "w") as f:
            for i in wrong_ids:
                f.write(f"{i}\n")

