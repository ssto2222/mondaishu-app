wrong_ids = load_wrong_ids()

if mode == "苦手克服モード":
    filtered_data = [q for q in QUIZ_DATA if str(q[0]) in wrong_ids]
    if not filtered_data:
        st.info("苦手な問題は登録されていません。")
        filtered_data = QUIZ_DATA
else:
    filtered_data = QUIZ_DATA

# 問題
